import mongoose from 'mongoose';
import bcryptjs from 'bcryptjs';

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
      match: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,
    },
    password: {
      type: String,
      required: true,
      minlength: 6,
    },
    avatar: {
      type: String,
      default: null,
    },
    resetToken: {
      type: String,
      default: null,
    },
    resetTokenExpiry: {
      type: Date,
      default: null,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  },
  { timestamps: true }
);

// Prevent duplicate email index error
userSchema.index({ email: 1 }, { unique: true, sparse: true });

// Instance method to compare passwords
userSchema.methods.comparePassword = async function (plainPassword: string) {
  return await bcryptjs.compare(plainPassword, this.password);
};

// Export User model
const User = mongoose.models.User || mongoose.model('User', userSchema);

export default User;
